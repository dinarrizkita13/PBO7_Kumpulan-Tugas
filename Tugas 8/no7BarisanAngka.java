
package tugaspertemuan8;
public class no7BarisanAngka {
    
    static void tampilkan(int[] arr) {
        for (int a : arr) {
            System.out.print(a + " ");
        }
    }

    public static void main(String[] args) {
        int[] angka = {6, 12, 24, 48, 96};
        
        tampilkan(angka);
    }
}

